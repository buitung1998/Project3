 <h4>Tuyên bố về quyền và trách nhiệm</h4>
<br />

	&nbsp;&nbsp;&nbsp;&nbsp;Thỏa thuận này được viết bằng tiếng Anh (Mỹ). Trong phạm vi bất kỳ bản dịch nào của thỏa thuận này xung đột với bản tiếng Anh thì bản tiếng Anh có quyền điều chỉnh.  Xin lưu ý rằng Mục 17 chứa những thay đổi nhất định về điều khoản chung cho người dùng ngoài Hoa Kỳ.
<br />

	Ngày sửa đổi gần đây nhất: Ngày 15 tháng 11 năm 2013.
<br />

<h4>Tuyên bố về quyền và trách nhiệm</h4>
<br />

&nbsp;&nbsp;&nbsp;&nbsp;Tuyên bố về quyền và trách nhiệm này ("Tuyên bố," "Điều khoản," hoặc "SRR") bắt nguồn từ Các nguyên tắc của Facebook và là điều khoản dịch vụ kiểm soát mối quan hệ của chúng tôi với người dùng và những người khác có tương tác với Facebook. Bằng cách sử dụng hoặc truy cập Facebook, bạn đồng ý với Tuyên bố này, như được cập nhật đôi lúc theo Mục 14 dưới đây. Ngoài ra, bạn sẽ thấy các tài nguyên ở cuối tài liệu này, giúp bạn hiểu được cách thức hoạt động của Facebook.
<br />

<h4>Quyền riêng tư</h4>
<br />

	&nbsp;&nbsp;&nbsp;&nbsp;Quyền riêng tư của bạn rất quan trọng với chúng tôi. Chúng tôi thiết kế Chính sách sử dụng dữ liệu nhằm đưa ra các tiết lộ quan trọng về cách bạn có thể dùng Facebook để chia sẻ với người khác và cách chúng tôi thu thập và có thể sử dụng nội dung và thông tin của bạn.  Chúng tôi khuyến khích bạn đọc Chính sách sử dụng dữ liệu và dùng chính sách đó để đưa ra các quyết định sáng suốt.
<br />
 
<h4>Chia sẻ nội dung và thông tin của bạn</h4>
<br />

	&nbsp;&nbsp;&nbsp;&nbsp;Bạn sở hữu tất cả các nội dung và thông tin bạn đăng trên Facebook và bạn có thể kiểm soát việc chia sẻ thông tin đó thông qua quyền riêng tư và thiết lập ứng dụng. Ngoài ra:
Đối với những nội dung có quyền sở hữu trí tuệ như ảnh và video (nội dung IP), bạn đặc biệt cho chúng tôi quyền sau tuân theo quyền riêng tư và thiết lập ứng dụng của bạn: bạn cấp cho chúng tôi một giấy phép không độc quyền, có thể chuyển nhượng, có thể cấp phép lại, miễn phí, toàn cầu để sử dụng mọi nội dung IP mà bạn đăng trên hoặc liên quan tới Facebook (Giấy phép IP). Giấy phép IP này kết thúc khi bạn xóa nội dung IP hoặc tài khoản của bạn trừ khi nội dung của bạn được chia sẻ với người khác và những người đó chưa xóa nội dung đó.<br />

&nbsp;&nbsp;&nbsp;&nbsp;Khi bạn xóa nội dung IP, thao tác đó tương tự như làm rỗng thùng rác trên máy tính. Tuy nhiên, bạn hiểu rằng nội dung bị xóa có thể vẫn còn trong các bản sao lưu trong một khoảng thời gian hợp lý (nhưng sẽ không khả dụng với những người khác).<br />

&nbsp;&nbsp;&nbsp;&nbsp;Khi bạn sử dụng một ứng dụng, ứng dụng đó có thể đề nghị bạn cho phép truy cập vào nội dung và thông tin của bạn cũng như nội dung và thông tin những người khác đã chia sẻ với bạn.  Chúng tôi yêu cầu các ứng dụng tôn trọng quyền riêng tư của bạn và thỏa thuận của bạn với ứng dụng đó sẽ kiểm soát cách thức ứng dụng có thể sử dụng, lưu trữ và chuyển nhượng nội dung và thông tin đó.  (Để tìm hiểu thêm về Nền tảng, bao gồm cách bạn có thể kiểm soát thông tin nào người khác có thể chia sẻ với ứng dụng, hãy đọc Chính sách sử dụng dữ liệu và Trang nền tảng của chúng tôi.)<br />

	
